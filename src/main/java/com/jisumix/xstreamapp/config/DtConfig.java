package com.jisumix.xstreamapp.config;

public class DtConfig {
	private BurstConfig burstConfig;
	
	private NumberConfig numberConfig;
	
	private AutoFillRule autoFillRule;

	public BurstConfig getBurstConfig() {
		return burstConfig;
	}

	public void setBurstConfig(BurstConfig burstConfig) {
		this.burstConfig = burstConfig;
	}

	public NumberConfig getNumberConfig() {
		return numberConfig;
	}

	public void setNumberConfig(NumberConfig numberConfig) {
		this.numberConfig = numberConfig;
	}

	public AutoFillRule getAutoFillRule() {
		return autoFillRule;
	}

	public void setAutoFillRule(AutoFillRule autoFillRule) {
		this.autoFillRule = autoFillRule;
	}
}
